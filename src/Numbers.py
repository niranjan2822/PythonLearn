# There are three numeric types in python
# int , float , complex -x = 1j

# example  : Integers

x = 1
y = 345621
z = -354433
print(type(x))
print(type(y))
print(type(z))

# Example - Float

x = 1.4
y = 2.10
z = -344.33
print(type(x))
print(type(y))
print(type(z))

# Example = Float can also be scientific numbers with an "e" to indicate the power of 10

x = 35e3
y = 12e4
z = -87.7e10
print(type(x))
print(type(y))
print(type(z))

# Example - Complex

x = 3+5j
y = 5j
z = -5j
print(type(x))
print(type(y))
print(type(z))



